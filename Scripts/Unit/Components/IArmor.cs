////////////////////////////////////////////////////////////////////////////////////
/**********************************************************************************/
// IArmor интерфейс
// интерфейс определяющий механику нанесения урона юниту - здоровье, шит
//
/**********************************************************************************/
public interface IArmor
{
    /**********************************************************************************/
    // эвент сообщающий о смерти юнита
    //
    /**********************************************************************************/
    event DestructionEvent UnitIsDown;

    /**********************************************************************************/
    // функция установки количества здоровья
    //
    /**********************************************************************************/
    void SetHealth(int health);


    /**********************************************************************************/
    // функция нанесения урона юниту
    //
    /**********************************************************************************/
    void TakeDamage(DamageData damage);

    /**********************************************************************************/
    // процессинговая функция
    //
    /**********************************************************************************/
    void Update();

    /**********************************************************************************/
    // сброс настроек
    //
    /**********************************************************************************/
    void ResetComponent();

}

